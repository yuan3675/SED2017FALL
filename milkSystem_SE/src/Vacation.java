public class Vacation {
    private int month;
    private int day;

    public Vacation(int month, int day) {
        this.day = day;
        this.month = month;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }
}
