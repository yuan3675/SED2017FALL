public class Company {
    private String category;
    private  String companyType;

    public void setCategory(String s) {
        this.category = s;
    }
    public  void setCompanyType(String s) {
        this.companyType = s;
    }

    public String getCategory() {
        return category;
    }

    public String getCompanyType() {
        return companyType;
    }
}
